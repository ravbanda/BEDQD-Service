package com.deloitte.bedqd.model;

public class DataQualityMonitering {
	private String header;
	private String dqScore;
	private String changeFromPriorQuarter;
	
	public String getHeader() {
		return header;
	}
	public void setHeader(String header) {
		this.header = header;
	}
	public String getDqScore() {
		return dqScore;
	}
	public void setDqScore(String dqScore) {
		this.dqScore = dqScore;
	}
	public String getChangeFromPriorQuarter() {
		return changeFromPriorQuarter;
	}
	public void setChangeFromPriorQuarter(String changeFromPriorQuarter) {
		this.changeFromPriorQuarter = changeFromPriorQuarter;
	}
	
	
	
}
