package com.deloitte.bedqd.model;

public class DQMoniteringStats {
	private String header;
	private String label;
	private String notDQMonitered;
	private String dqMonitered;
	private String yearQuarter;
	private String lob;
	private String sourceSystem;
	
	public String getHeader() {
		return header;
	}
	public void setHeader(String header) {
		this.header = header;
	}
	public String getLabel() {
		return label;
	}
	public void setLabel(String label) {
		this.label = label;
	}
	public String getNotDQMonitered() {
		return notDQMonitered;
	}
	public void setNotDQMonitered(String notDQMonitered) {
		this.notDQMonitered = notDQMonitered;
	}
	public String getDqMonitered() {
		return dqMonitered;
	}
	public void setDqMonitered(String dqMonitered) {
		this.dqMonitered = dqMonitered;
	}
	public String getYearQuarter() {
		return yearQuarter;
	}
	public void setYearQuarter(String yearQuarter) {
		this.yearQuarter = yearQuarter;
	}
	public String getLob() {
		return lob;
	}
	public void setLob(String lob) {
		this.lob = lob;
	}
	public String getSourceSystem() {
		return sourceSystem;
	}
	public void setSourceSystem(String sourceSystem) {
		this.sourceSystem = sourceSystem;
	}
	
	
	
}
