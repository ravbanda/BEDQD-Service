package com.deloitte.bedqd.model;

public class OpenDQIssuesSummaryModel {
	
	private String legalEntity;
	private String yearQuarter;
	private String sourceSystem;
	private String lob;
	private String priorQuarter;
	private String currentQuarter;
	private String change;
	
	public String getLegalEntity() {
		return legalEntity;
	}
	public void setLegalEntity(String legalEntity) {
		this.legalEntity = legalEntity;
	}
	public String getYearQuarter() {
		return yearQuarter;
	}
	public void setYearQuarter(String yearQuarter) {
		this.yearQuarter = yearQuarter;
	}
	public String getSourceSystem() {
		return sourceSystem;
	}
	public void setSourceSystem(String sourceSystem) {
		this.sourceSystem = sourceSystem;
	}
	public String getLob() {
		return lob;
	}
	public void setLob(String lob) {
		this.lob = lob;
	}
	public String getPriorQuarter() {
		return priorQuarter;
	}
	public void setPriorQuarter(String priorQuarter) {
		this.priorQuarter = priorQuarter;
	}
	public String getCurrentQuarter() {
		return currentQuarter;
	}
	public void setCurrentQuarter(String currentQuarter) {
		this.currentQuarter = currentQuarter;
	}
	public String getChange() {
		return change;
	}
	public void setChange(String change) {
		this.change = change;
	}
	
	

}
